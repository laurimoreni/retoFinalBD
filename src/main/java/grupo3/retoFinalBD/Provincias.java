package grupo3.retoFinalBD;

public class Provincias {

	private int id;
	private String nombre;

	public Provincias() {
		super();
	}
	
	public Provincias(int id, String nombre) {
		super();
		this.id = id;
		this.nombre = nombre;
	}

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}
}


