package grupo3.retoFinalBD;

public class Provincia {

	private int id;
	private String nombre;

	public Provincia() {
	}
	
	public Provincia(int id, String nombre) {
		this.id = id;
		this.nombre = nombre;
	}

	public int getId() {
		return id;
	}

	public String getNombre() {
		return nombre;
	}
}


